package com.hisilicon.dlna.dmc.processor.model;

public class LocalPhoto {

	private long id;
	private String title;
	private long dateCreated;
	private String day;
	private String folder;
	private String mimeType;
	private String month;
	private String monthDisplayName;
	private String path;
	private long size;
	private String year;
	private long thumbId;
	private String thumbPath;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public long getDateCreated() {
		return dateCreated;
	}
	public void setDateCreated(long dateCreated) {
		this.dateCreated = dateCreated;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getFolder() {
		return folder;
	}
	public void setFolder(String folder) {
		this.folder = folder;
	}
	public String getMimeType() {
		return mimeType;
	}
	public void setMimeType(String mimeType) {
		this.mimeType = mimeType;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getMonthDisplayName() {
		return monthDisplayName;
	}
	public void setMonthDisplayName(String monthDisplayName) {
		this.monthDisplayName = monthDisplayName;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public long getSize() {
		return size;
	}
	public void setSize(long size) {
		this.size = size;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public long getThumbId() {
		return thumbId;
	}
	public void setThumbId(long thumbId) {
		this.thumbId = thumbId;
	}
	public String getThumbPath() {
		return thumbPath;
	}
	public void setThumbPath(String thumbPath) {
		this.thumbPath = thumbPath;
	}
}
