HI_S32 temperature_control_thread(void *Arg)
{
    HI_U16 u16Temperature;
    HI_U16 u16LastTemperature = 0;
    HI_U16 u16AddCount = 0;
    HI_U16 u16MinusCount = 0;
    HI_U32 u32CpuFreq;
    HI_U32 u32OriCpuFreq = DEFAULT_INIT_FREQ;
    HI_U8 u8TimeInterval = 100;
    HI_U8 i;
    HI_BOOL bIncreaseFreq = HI_FALSE;
    HI_BOOL bNeedReboot = HI_TRUE;
    HI_U32 u32RegVal;

    printk("enter temperature_control_thread\n");

    /* Init tsensor */
    //HI_REG_WRITE32(PERI_PMC10, 0x07200000);
    HI_REG_READ32(IO_ADDRESS(MCU_START_REG), u32RegVal);

    if (0 == u32RegVal)
    {
        c51_loadCode();
        HI_REG_WRITE32(IO_ADDRESS(MCU_START_REG), 0x1); //start MCU
    }

    g_u8TempUp = TEMP_UP;
    g_u8TempDown = TEMP_DOWN;
    g_u8TempReboot = TEMP_REBOOT;
    g_u8TempCtrlEnable = HI_TRUE;

    while (!kthread_should_stop())
    {
        msleep(u8TimeInterval);

        if (HI_FALSE == g_u8TempCtrlEnable)
        {
            continue;
        }

        mpu_get_temperature(&u16Temperature);

        if (u16Temperature < g_u8TempUp)
        {
            if (HI_FALSE == cpu_dvfs_enable)
            {
                for (i = 0; i < MAX_FREQ_NUM; i++)
                {
                    if (u32OriCpuFreq == cpu_freq_hpm_table[i].freq)
                    {
                        break;
                    }
                }

                if (i >= MAX_FREQ_NUM)
                {
                    continue;
                }

                u32CpuFreq = clk_get_rate(&mpu_ck);
                if (u32CpuFreq > u32OriCpuFreq)
                {
                    clk_set_rate(&mpu_ck, u32OriCpuFreq);
                    msleep(10);
                    cpu_volt_scale(cpu_freq_hpm_table[i].vsetting);
                }
                else
                {
                    cpu_volt_scale(cpu_freq_hpm_table[i].vsetting);
                    msleep(10);
                    clk_set_rate(&mpu_ck, u32OriCpuFreq);
                }

                cpu_dvfs_enable = HI_TRUE;
            }

            bNeedReboot = HI_TRUE;
            u8TimeInterval = 100;
            continue;
        }
        else if ((g_u8TempDown <= u16Temperature) && (u16Temperature <= g_u8TempReboot))
        {
            /* stop dvfs */
            if (HI_TRUE == cpu_dvfs_enable)
            {
                cpu_dvfs_enable = HI_FALSE;

                u32OriCpuFreq = clk_get_rate(&mpu_ck);
                HI_INFO_PM("u32OriCpuFreq = %d \n", u32OriCpuFreq);
            }

            u32CpuFreq = clk_get_rate(&mpu_ck);
            for (i = 0; i < MAX_FREQ_NUM; i++)
            {
                if (u32CpuFreq == cpu_freq_hpm_table[i].freq)
                {
                    break;
                }
            }

            if (i >= MAX_FREQ_NUM)
            {
                i = 0;
            }

            if (u16Temperature > u16LastTemperature)
            {
                u16AddCount++;
                u16MinusCount = 0;
            }
            else if (u16Temperature < u16LastTemperature)
            {
                u16AddCount = 0;
                u16MinusCount++;
            }

            u16LastTemperature = u16Temperature;

            if (u16AddCount >= 3)
            {
                if (i > 0)
                {
                    i = i - 1;
                }
                bIncreaseFreq = HI_FALSE;
                u16AddCount = 0;
            }

            if (u16MinusCount >= 3)
            {
                if (i < (MAX_FREQ_NUM - 1))
                {
                    i = i + 1;
                }
                u16MinusCount = 0;
                bIncreaseFreq = HI_TRUE;
            }

            if ((u32CpuFreq != cpu_freq_hpm_table[i].freq) && (u32OriCpuFreq > cpu_freq_hpm_table[i].freq))
            {
                if (HI_TRUE == bIncreaseFreq)
                {
                    cpu_volt_scale(cpu_freq_hpm_table[i].vsetting);
                    msleep(10);
                    clk_set_rate(&mpu_ck, cpu_freq_hpm_table[i].freq);
                }
                else
                {
                    clk_set_rate(&mpu_ck, cpu_freq_hpm_table[i].freq);
                    msleep(10);
                    cpu_volt_scale(cpu_freq_hpm_table[i].vsetting);
                }
                HI_INFO_PM("set freq to %d, u16Temperature = %d \n", cpu_freq_hpm_table[i].freq, u16Temperature);
            }

            bNeedReboot = HI_TRUE;
            u8TimeInterval = 20;
        }
        else if (u16Temperature >= g_u8TempReboot)
        {
            if (HI_TRUE == cpu_dvfs_enable)
            {
                /* stop dvfs */
                cpu_dvfs_enable = HI_FALSE;

                /* set freq to low */
                clk_set_rate(&mpu_ck, cpu_freq_hpm_table[0].freq);
                msleep(10);
                cpu_volt_scale(cpu_freq_hpm_table[0].vsetting);

                /* in the process of start system, do not reboot system  */
                bNeedReboot = HI_FALSE;
            }
            else
            {
                if (HI_TRUE == bNeedReboot)
                {
                    HI_ERR_PM("Tsensor is extremly high, reboot system!!\n");
                    //kernel_restart(NULL);
                    HI_REG_WRITE32(IO_ADDRESS(0xf840e520), 0x11111111);
                }
            }
        }
    }

    return HI_SUCCESS;
}
