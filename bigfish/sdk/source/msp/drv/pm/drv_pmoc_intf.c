void mpu_get_temperature(HI_U16 * u16Temp)
{
    HI_U32 u32RegVal;
    HI_U8 i, j;
    HI_U16 u16TsensorAverage = 0;

#if defined(CHIP_TYPE_hi3798cv200_a)
    HI_REG_WRITE32(PERI_PMC10, 0x72);

    for (j = 0; j < 4; j++)
    {
        HI_REG_READ32(PERI_PMC12 + 0x4 * j, u32RegVal);
        for (i = 0; i < 2; i++)
        {
            u16TsensorAverage += ((u32RegVal >> (16 * i)) & 0x3ff);
        }
    }
#else
    HI_REG_WRITE32(PERI_PMC10, 0x07200000);

    for (j = 0; j < 2; j++)
    {
        HI_REG_READ32(PERI_PMC12 + 0x4 * j, u32RegVal);
        for (i = 0; i < 4; i++)
        {
            u16TsensorAverage += ((u32RegVal >> (8 * i)) & 0xff);
        }
    }
#endif

    *u16Temp = (u16TsensorAverage / 8);

    return;
}

